package com.example.pizzaordering;
public class pizza {
    int Size;
    int Bacon;
    int Salami;
    int blackOlives;
    public pizza() {
    }
//setters and getters
    public int getSize() {
        return Size;
    }

    public void setSize(int Size) {
        this.Size = Size;
    }

    public int getBacon() {
        return Bacon;
    }

    public void setBacon(int Bacon) {
        Bacon = Bacon;
    }

    public int getSalami() {
        return Salami;
    }

    public void setSalami(int Salami) {
        Salami = Salami;
    }

    public int getblackOlives() {
        return blackOlives;
    }

    public void setblackOlives(int blackOlives) {
        blackOlives = blackOlives;
    }
}
